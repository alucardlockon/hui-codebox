package cn.alucardlockon.codebox.entity;

public class Rect {
    private String x;

    private String y;

    private String width;

    private String heght;

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }

    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }

    public String getHeght() {
        return heght;
    }

    public void setHeght(String heght) {
        this.heght = heght;
    }
}
